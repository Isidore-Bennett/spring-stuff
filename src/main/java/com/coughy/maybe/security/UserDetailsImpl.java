package com.coughy.maybe.security;

import com.coughy.maybe.entity.User;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.Collections;
import java.util.List;

public class UserDetailsImpl implements UserDetails {

    String userName;
    String password;
    List<GrantedAuthority> grantedAuthorities;

    public UserDetailsImpl(User user) {
        this.userName = user.getLogin();
        this.password = user.getPassKey();
        this.grantedAuthorities = Collections.singletonList(
                new SimpleGrantedAuthority(user.getUserProfile().getTitle())
        );
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return grantedAuthorities;
    }

    @Override
    public String getPassword() {
        return password;
    }

    @Override
    public String getUsername() {
        return userName;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
